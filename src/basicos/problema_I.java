public class problema_I {

    public static void main(String[] args) {

        // Incremento de una variable en 1
        int x = 4;
        x++; // Incrementa x en 1
        System.out.println("El valor de X después del incremento es: " + x);

        // Decremento de una variable en 1
        int y = 6;
        y--; // Decrementa y en 1
        System.out.println("El valor de Y después del decremento es: " + y);

    }

}
